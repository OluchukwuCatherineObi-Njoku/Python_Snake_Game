import turtle # import the turtle module

# create a new turtle object

my_turtle = turtle.Turtle() # create a new turtle object 
    #turtle is a module, Turtle is a class
my_turtle.shape("turtle") # set the shape to a turtle
my_turtle.color("red") # set the color to red

# move the turtle forward 100 pixels
my_turtle.forward(100) # move forward 100 pixels

turtle.done() # keep the window open until it is closed manually

